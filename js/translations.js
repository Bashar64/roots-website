let currentLang = 'en';

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    window.currentLang = currentLang;
    applyTranslations();
}

function applyTranslations() {
    // Translate text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });

    // Translate the button itself
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.textContent = translations[currentLang].lang_btn;
    }

    // Set document direction for correct rendering (useful for RTL languages)
    document.documentElement.dir = translations[currentLang].dir;
}

// Expose globally so main.js can call them after dynamic loading
window.currentLang = currentLang;
window.applyTranslations = applyTranslations;
window.initLanguageToggler = function() {
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.replaceWith(langBtn.cloneNode(true));
        const newLangBtn = document.getElementById('lang-toggle');
        newLangBtn.addEventListener('click', toggleLanguage);
    }
};

// Initialize event listener
document.addEventListener('DOMContentLoaded', () => {
    window.initLanguageToggler();
});
